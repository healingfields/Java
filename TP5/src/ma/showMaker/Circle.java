package ma.showMaker;

import java.awt.*;

public class Circle {

    int x,y,diameter,dx,dy;
    Color color;

    public Circle(int x,int y,int diameter,int dx,int dy,Color color){
        this.x = x;
        this.y = y;
        this.diameter = diameter;
        this.dx = dx;
        this.dy = dy;
        this.color=color;
    }

}
